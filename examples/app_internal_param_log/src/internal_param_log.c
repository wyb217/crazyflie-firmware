#include <string.h>
#include <stdint.h>
#include <stdbool.h>

#include "app.h"

#include "FreeRTOS.h"
#include "task.h"

#include "debug.h"
#include "uart2.h"
#include "log.h"
#include "param.h"

#define DEBUG_MODULE "INTERNLOGPARAM"

void appMain()
{
  uart2Init(115200);
  uint8_t ctr[] = {0xFF, 0x00, 0x01};
  while (1)
  {
    vTaskDelay(M2T(2000));

    uart2SendData(3, (uint8_t *)ctr);
  }
}